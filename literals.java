public class literals {
    public static void main(String[] args) {
        // literals

        int num1 = 0b111;
        int num2 = 0x7E;
        System.out.println(num1);
        System.out.println(num2);
        // Count number of zero's

        int num3 = 10_00_00_00_0;
        System.out.println(num3);
        char c = 'a';
        System.out.println(c);
        // c++;

        System.out.println(c);
    }
}
